// Создать переменные name, address, phone в которых с помощью prompt() будет
// храниться значения имени, адреса и номер телефона. Вывести в консоль текст
// “Студент . Адрес: , Номер телефона: ___” где вместо пропусков будут соответствующие переменные.
let name = prompt("Введите свое имя: ");
let address = prompt("Введите свой адрес: ");
let phone = prompt("Введите номер телефона: ");

alert(`Студент ${name}. Адрес: ${address}, Номер телефона: ${phone}`);
